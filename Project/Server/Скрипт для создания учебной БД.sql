-- Описание для триггера UC_STREET_ABONENT
--
CREATE OR REPLACE TRIGGER UC_STREET_ABONENT
AFTER UPDATE
ON STREET
FOR EACH ROW
WHEN (NEW.streetcd != old.streetcd)
BEGIN
UPDATE ABONENT
SET streetcd = :new.streetcd
WHERE streetcd = :old.streetcd;
END;
GO
